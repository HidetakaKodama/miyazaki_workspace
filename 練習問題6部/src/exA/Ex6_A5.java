package exA;

public class Ex6_A5 {
	public static void main(String[] args) {
		int num = 10;
		int sum = sum(num);
		System.out.println("1から" + num + "までの和は" + sum + "です");
	}
	public static int sum(int num) {
		for (int i = 1; 0 == num; i--) {
			int sum = num + i;
		int result = sum;
		return result;
		}
	}
}
