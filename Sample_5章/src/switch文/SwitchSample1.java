class SwitchSample1 {
	public static void main(String[] args) {
		int num = 20;
		switch (num) {
			case 10:
				System.out.println("A");
				break;
			case 20:
				System.out.println("B");
				break;
			case 30:
				System.out.println("C");
				break;
			default:
				System.out.println("D");
		}
	}

}
